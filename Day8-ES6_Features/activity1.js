// Task 1 : Use Template Literals to create a string

const personName = "Jane Doe";
const personAge = 30;

const str = `The name of person is ${personName} and the age of person is ${personAge}.`;

console.log(str); // The name of person is Jane Doe and the age of person is 30.

console.log();

// Task 2 : Create a multi-line string using template literal

const multiLineString = `The person name is ${personName}
The person age is ${personAge}`;

console.log(multiLineString);
/* Output : 

The person name is Jane Doe
The person age is 30

*/