// Task 3 : Use push() method

const arr = [34, 23, 19, 57, 64, 20];

arr.push(89); // On logging this arr.push(89), we get new length of array

console.log(arr); // [34, 23, 19, 57, 64, 20, 89]

console.log();

// Task 4 : Use pop() method

arr.pop();
console.log(arr); // [ 34, 23, 19, 57, 64, 20 ]

console.log();

// Task 5 : Use shift() method

arr.shift();
console.log(arr); //[ 23, 19, 57, 64, 20 ]

console.log();

// Task 6 : Use unshift() method

arr.unshift(6);
console.log(arr); // [ 6, 23, 19, 57, 64, 20 ]
